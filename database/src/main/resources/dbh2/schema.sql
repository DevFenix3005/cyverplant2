CREATE TABLE PUBLIC.CONFIGURATION_OF_NODES
(
    NODE_ID   BIGINT                 NOT NULL AUTO_INCREMENT,
    NODE_NAME CHARACTER VARYING(150) NOT NULL,
    NODE_TYPE CHARACTER VARYING(150) NOT NULL,
    NODE_URL  CHARACTER VARYING(150) NOT NULL,
    CONSTRAINT NODE_PKEY PRIMARY KEY (NODE_ID)
);
CREATE UNIQUE INDEX NODE_NAME_AND_TYPE_INDEX ON PUBLIC.CONFIGURATION_OF_NODES (NODE_NAME, NODE_TYPE);
CREATE UNIQUE INDEX PRIMARY_KEY_6 ON PUBLIC.CONFIGURATION_OF_NODES (NODE_ID);
